    private void setTableData(List<User_dthkhanh> users) {
        for (User_dthkhanh user : users) {
            defaultTableModel.addRow(new Object[]{user.getId(), user.getName(), user.getPhone(), user.getUsername(),
                user.getPassword(), user.getRole(), user.getFavourites(), user.getAbout()});

        }
    }

