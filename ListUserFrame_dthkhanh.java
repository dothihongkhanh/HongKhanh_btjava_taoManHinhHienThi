    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = userTable.getSelectedRow();
        if(row == -1)//nguoi dung chua chon hang nao
        {
            JOptionPane.showMessageDialog(ListUserFrame_dthkhanh.this, "Vui long chon user truoc", "loi",JOptionPane.ERROR_MESSAGE);
            
        }else{
            int confirm = JOptionPane.showConfirmDialog(ListUserFrame_dthkhanh.this, "Ban chac chan muon xoa khong?");
            if ( confirm == JOptionPane.YES_OPTION){
                
                
                try {
                    int userId = Integer.valueOf(String.valueOf(userTable.getValueAt(row, 0)));
                    
                    userService.deleteUser(userId);
                    
                    defaultTableModel.setRowCount(0);//de xoa het du lieu hien tai
                    setTableData(userService.getAllUsers());
                } catch (SQLException ex) {
                    Logger.getLogger(ListUserFrame_dthkhanh.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                }
            }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

